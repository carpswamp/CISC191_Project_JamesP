import java.util.List;

/**
 * This class is meant to act as an interface for calculator classes.
 * The purpose of the interface is to foster consistency throughout the calculator classes, allow for scaling,
 * and facilitate testing.
 */
public abstract class Calculator
{	
    protected static final double ELECTROSTATIC_CONSTANT = 8.9875517923e9; 	// Constant (k), in Nm²/C²
    protected static final double ELECTRON_CHARGE = 1.602176634e-19; 		// Charge of one electron, in C

}
